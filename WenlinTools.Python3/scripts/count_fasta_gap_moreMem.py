    gap_chars = ['N', '-']
    seqDict = read_fa(fn)
    for name in seqDict:
        seq = seqDict[name]
        Ngap = 0
        for char in gap_chars:
            Ngap += seq.count(char)
        print(name, len(seq), Ngap, float(Ngap)/len(seq))




